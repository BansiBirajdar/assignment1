'''Write a program which display 5 times Marvellous on screen.
Output : Marvellous
        Marvellous
        Marvellous
        Marvellous
        Marvellous'''

def main():
    no=int(input("Enter the number how many time display="));
    print("output=")
    for i in range(0,no):
        print("\tMarvellous");


if(__name__=="__main__"):
    main();